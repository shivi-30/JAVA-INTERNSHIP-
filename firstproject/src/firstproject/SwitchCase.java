package firstproject;

public class SwitchCase {
public static void main(String[] args) {
		
		// Switch Case
		
		int number = 10;
		
		switch(number) {
		  case 1:
		   System.out.println("10");
		    break;
		  case 2:
			  System.out.println("20");
		    break;
		  default:
			  System.out.println("Not a Number");
		}

	}
}
